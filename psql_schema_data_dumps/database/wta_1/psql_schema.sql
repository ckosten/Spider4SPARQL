create table if not exists players(
         player_id int primary key,
         first_name text,
         last_name text,
         hand text,
         birth_date date,
         country_code text
     );
     create table if not exists matches(
       best_of int,
       draw_size int,
       loser_age float,
       loser_entry text,
       loser_hand text,
       loser_ht int,
       loser_id int,
       loser_ioc text,
       loser_name text,
       loser_rank int,
       loser_rank_points int,
       loser_seed int,
       match_num int,
       minutes int,
       round text,
       score text,
       surface text,
       tourney_date date,
       tourney_id text,
       tourney_level text,
       tourney_name text,
       winner_age float,
       winner_entry text,
       winner_hand text,
       winner_ht int,
       winner_id int,
       winner_ioc text,
       winner_name text,
       winner_rank int,
       winner_rank_points int,
       winner_seed int,
       year int,
       foreign key(loser_id) references players(player_id),
       foreign key(winner_id) references players(player_id)
     );
     create table if not exists rankings(ranking_date date,ranking int,player_id int,ranking_points int,tours int,foreign key(player_id) references players(player_id));
